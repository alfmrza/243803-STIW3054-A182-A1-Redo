public class SizeofSoftware {
	double sZUC;

	public double calcSZUC() {
		UUCP uUCP = new UUCP();
		uUCP.UUCP();
		TotalTechnicalComplexityFactor tTCF = new TotalTechnicalComplexityFactor();
		tTCF.calcTCF();
		double sZUC = uUCP.getUUCP() * tTCF.getTTCF();
		System.out.println("The size of software is = " + sZUC);
		return sZUC;
	}

	public double getSZUC() {
		return sZUC;
	}
}


